#include <stdint.h>
#include <stdio.h>
#include <stddef.h>

struct couleur {
    uint8_t r;
    uint8_t g;
    uint8_t b;
    uint8_t a;
};

int main(void) {
    const struct couleur couleurs[10] = {
        {0xEF, 0x78, 0x12, 0xFF}, {0x2C, 0xC8, 0x64, 0xFF},
        {0x00, 0x7A, 0xCC, 0xFF}, {0xFF, 0xAA, 0x00, 0xFF},
        {0x10, 0x10, 0x10, 0xFF}, {0xFA, 0x5B, 0xAA, 0xFF},
        {0x1A, 0xB5, 0xF0, 0xFF}, {0x33, 0x99, 0x66, 0xFF},
        {0xCC, 0x33, 0x99, 0xFF}, {0x44, 0x55, 0x66, 0xFF}};

    for (size_t i = 0; i < 10; ++i) {
        printf("Couleur %zu : R=%3u G=%3u B=%3u A=%3u\n", i + 1, couleurs[i].r,
               couleurs[i].g, couleurs[i].b, couleurs[i].a);
    }

    return 0;
}

